package models.operations;

public class Location {
	private double lan;
	private double lng;
	
	public Location() {
		this.lan = 5.3;
		this.lng = 2.7;
	}

	public double getLan() {
		return lan;
	}

	public void setLan(double lan) {
		this.lan = lan;
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}
	
	
	
	
}
